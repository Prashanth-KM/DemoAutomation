package Loops;

public class ForLoop {
    public static void main(String[] args) {
        for(int i=10;i<=20;i++){
            System.out.println(i);
            //i=10, 10<=20-- first iteration
            //10-- 10+1=11

            //i=11, 11<=20-- second
            //11-- 11+1=12

            //i=12, 12<=20-- third
            //12-- 12+1=13

            //i=13, 13<=20-- fourth
            //13-- 13+1=14

            //i=14, 14<=20--fifth
            //14-- 14+1=15

            //i=15, 15<=20--sixth
            //15-- 15+1=16

            //i=16, 16<=20--seven
            //16-- 16+1=17

            //i=17, 17<=20--eight
            //17-- 17+1=18

            //i=18, 18<=20
            //18-- 18+1=19

            //1=19, 19<=20
            //19-- 19+1=20

            //i=20, 20<=20
            //20-- 20+1=21

            //i=21, 21<=20


        }
        System.out.println("******************");
        for(int j=20;j>=10;j--){
            System.out.println(j);
        }
    }
}
